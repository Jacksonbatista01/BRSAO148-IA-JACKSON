'''9 - Crie um programa que faça uma contagem regressiva de 5 até 1 usando while, e exiba “Lançar!” no final.'''

# Define o número inicial da contagem
contador = 5

# Laço while para contagem regressiva
while contador >= 1:
    print(contador)
    contador -= 1  # Decrementa 1 a cada repetição

# Exibe a mensagem final
print("Lançar!")