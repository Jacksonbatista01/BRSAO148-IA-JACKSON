# Fun√ß√£o para cadastrar alunos e suas notas
def cadastrar_alunos():
    alunos = {}

    while True:
        nome = input("\nDigite o nome do aluno (ou 'fim' para encerrar): ").strip()
        if nome.lower() == "fim":
            break
        if nome == "":
            print("‚ö†Ô∏è Nome n√£o pode ser vazio.")
            continue

        notas = []
        for i in range(1, 4):
            while True:
                try:
                    nota = float(input(f"Digite a {i}¬™ nota de {nome} (0 a 10): "))
                    if 0 <= nota <= 10:
                        notas.append(nota)
                        break
                    else:
                        print("‚ö†Ô∏è Nota fora do intervalo permitido (0 a 10).")
                except ValueError:
                    print("‚ö†Ô∏è Entrada inv√°lida. Digite um n√∫mero.")

        alunos[nome] = notas
        print(f"‚úÖ Aluno {nome} cadastrado com sucesso.")

    return alunos

# Fun√ß√£o para calcular e exibir as m√©dias
def exibir_resultados(alunos):
    print("\n=== M√©dias dos Alunos ===")
    maior_media = -1
    aluno_top = ""

    for nome, notas in alunos.items():
        media = sum(notas) / len(notas)
        print(f"{nome}: M√©dia = {media:.2f}")
        if media > maior_media:
            maior_media = media
            aluno_top = nome

    if aluno_top:
        print(f"\nüèÜ Aluno com a maior m√©dia: {aluno_top} ({maior_media:.2f})")
    else:
        print("Nenhum aluno cadastrado.")

# Programa principal
print("=== Cadastro de Alunos e Notas ===")
alunos_cadastrados = cadastrar_alunos()
exibir_resultados(alunos_cadastrados)